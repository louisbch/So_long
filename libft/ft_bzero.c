#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	char	*ptr_s;
	size_t	i;

	ptr_s = (char *)s;
	i = 0;
	while (i < n)
	{
		ptr_s[i] = '\0';
		i++;
	}
}
