#include "libft.h"

char	*ft_strdup(const char *src)
{
	size_t	i;
	char	*dest;

	dest = malloc(ft_strlen(src) * sizeof(char) + 1);
	if (!dest)
		return (NULL);
	i = 0;
	while (src[i])
	{
		dest[i] = src[i];
		i++;
	}
	dest[i] = '\0';
	return (dest);
}
